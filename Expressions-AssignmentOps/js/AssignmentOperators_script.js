//Expressions - Assignment Operators

var a = 3;
a += 4; //a = a + 4
//console.log(a);


var a = 3;
a *= 4; //a = a * 4          
//console.log(a);


var a = 3;
a /= 4; //a = a / 4
//console.log(a);


var a = 3;
a -= 4; //a = a - 4
//console.log(a);


var a = 3;
a ++; //a = a + 1 // a += 1
//console.log(a);


var a = 3;
a --; //a = a - 1 // a -= 1
console.log(a);